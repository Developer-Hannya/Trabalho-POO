    private void salvarJogador(Jogador j) {
        try{
        FileOutputStream fos = new FileOutputStream("Jogador.ser");
        ObjectOutputStream oos = new ObjectOutputStream(fos);
        oos.writeObject(j);
        oos.flush(); 
        
        
        
        } catch(Exception ex){
            
        }
        
    }
    
    private void criaBatalha(Jogador j) {
        salvarJogador(j);
        Batalha batalha;
        batalha = new Batalha();
        
        batalha.setVisible(true);
        this.dispose();
    }
